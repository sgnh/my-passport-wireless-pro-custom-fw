static int mAudioAmp_GetMappedSubWoofVolume(int index)
{
	int volTableAtv[12] = { 50, 42, 38, 34, 30, 26, 22, 16, 10, 7, 3, 1};  //tlm 20111111 32e83
	int *pVolTable = volTableAtv;
	int value = 0;

	if (index == 0)
 		value = 0xff;
	else if (index == 1)
		value = pVolTable[1];
	else {
		int preIdx = 0, nextIdx = 0;
		int tableIdx = 0, preTableIdx = 0;

		if (index > 1 && index <= 10) {
			preIdx = 1;
			nextIdx = 10;
			tableIdx = 2;
			preTableIdx = 1;
		} else if (index > 10 && index <= 20) {
			preIdx = 10;
			nextIdx = 20;
			tableIdx = 3;
			preTableIdx = 2;
		} else if (index > 20 && index <= 30) {
			preIdx = 20;
			nextIdx = 30;
			tableIdx = 4;
			preTableIdx = 3;
		} else if (index > 30 && index <= 40) {
			preIdx = 30;
			nextIdx = 40;
			tableIdx = 5;
			preTableIdx = 4;
		} else if (index > 40 && index <= 50) {
			preIdx = 40;
			nextIdx = 50;
			tableIdx = 6;
			preTableIdx = 5;
		} else if (index > 50 && index <= 60) {
			preIdx = 50;
			nextIdx = 60;
			tableIdx = 7;
			preTableIdx = 6;
		} else if (index > 60 && index <= 70) {
			preIdx = 60;
			nextIdx = 70;
			tableIdx = 8;
			preTableIdx = 7;
		} else if (index > 70 && index <= 80) {
			preIdx = 70;
			nextIdx = 80;
			tableIdx = 9;
			preTableIdx = 8;
		} else if (index > 80 && index <= 90) {
			preIdx = 80;
			nextIdx = 90;
			tableIdx = 10;
			preTableIdx = 9;
		} else if (index > 90 && index <= 100) {
			preIdx = 90;
			nextIdx = 100;
			tableIdx = 11;
			preTableIdx = 10;
		}

		//interpolation
		value = (int)((float)((index - preIdx) * (pVolTable[tableIdx] - pVolTable[preTableIdx])) / (nextIdx - preIdx));
		value += pVolTable[preTableIdx];
	}

	return value;
}

